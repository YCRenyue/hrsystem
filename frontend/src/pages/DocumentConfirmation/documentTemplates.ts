// ---------------------------------------------------------------------------
// Interfaces
// ---------------------------------------------------------------------------

export interface DocumentSection {
  heading: string;
  content: string;
  subItems?: string[];
}

export interface DocumentPage {
  pageTitle: string;
  sections: DocumentSection[];
}

export interface DocumentTemplate {
  title: string;
  version: string;
  preamble: string;
  sections: DocumentSection[];
  closingStatement: string;
  /** When true the signing page renders in multi-page reading mode */
  paginated?: boolean;
  /** Used when paginated === true */
  pages?: DocumentPage[];
}

// ---------------------------------------------------------------------------
// Utilities
// ---------------------------------------------------------------------------

/**
 * Replace all {variable} placeholders in a template with values from vars.
 * Unknown keys are shown as [key] to make missing data obvious.
 */
export function renderTemplate(
  template: DocumentTemplate,
  vars: Record<string, string>
): DocumentTemplate {
  const replace = (text: string): string =>
    text.replace(/\{(\w+)\}/g, (_, key) => vars[key] ?? `[${key}]`);

  const replaceSection = (s: DocumentSection): DocumentSection => ({
    ...s,
    content: replace(s.content),
    subItems: s.subItems?.map(replace),
  });

  return {
    ...template,
    preamble: replace(template.preamble),
    sections: template.sections.map(replaceSection),
    closingStatement: replace(template.closingStatement),
    pages: template.pages?.map((page) => ({
      ...page,
      sections: page.sections.map(replaceSection),
    })),
  };
}

/**
 * Convert a positive integer (yuan amount) to Chinese capital form.
 * e.g. 5000 -> "伍仟元整"
 */
export function numberToChinese(num: number): string {
  const digits = ["零", "壹", "贰", "叁", "肆", "伍", "陆", "柒", "捌", "玖"];
  const units = ["", "拾", "佰", "仟"];
  const bigUnits = ["", "万", "亿"];

  if (!num || num <= 0) return "零元整";

  const intPart = Math.floor(num);
  const intStr = String(intPart);
  const len = intStr.length;

  let result = "";
  let prevZero = false;

  for (let i = 0; i < len; i++) {
    const d = parseInt(intStr[i]);
    const pos = len - 1 - i;
    const unitIdx = pos % 4;
    const bigIdx = Math.floor(pos / 4);

    if (d !== 0) {
      if (prevZero) result += "零";
      result += digits[d] + units[unitIdx];
      prevZero = false;
    } else {
      prevZero = true;
    }

    if (unitIdx === 0 && i < len - 1) {
      result += bigUnits[bigIdx];
      prevZero = false;
    }
  }

  return result + "元整";
}

